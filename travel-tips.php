                <li>Our top four is in Italy again. And is called lake Como. Yes it can be touristy but if you go in Autumn or in Spring you have great temperatures and the old little towns around lake Como are breathtaking with the purple autumn colors. You can fly cheap to Milan in an hour and take a short train ride from there to lake Como.</li>
                <br>
                <li>Our top five destination is Portugal. Lisbon is one of the most beautiful European cities with a rich history. Also the wine areas around Lisbon and the beaches in the south of Portugal will not disappoint you.</li>
