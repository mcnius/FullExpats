              <ul style="list-style:decimal">
                <li>Our number one is the Greece islands with Santorini as favorite island. The views from these towns on this island which are located 500 meter above the sea on a volcano are breathtaking. Transavia flies daily to Santorini.</li>
                <br>
                <li>Our second favorite destination: Cinque terre in Italy. It is a very colorful and beautiful part of Italy with amazing views.  You can hike to each of the beautiful 5 towns and take a little train back when you are tired. You can fly to Pisa for already 40 euro and take a train from there.</li>
                <br>
                <li>Our number three Switzerland. Yes it is quite expensive but if you love mountains and lakes it is the perfect destination. It is a very safe and clean country and the views will give you goosebumps.</li>
