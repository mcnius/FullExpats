              <p>
                Moving to another country is for most people a dream. You will wider your horizons and immerse yourself into a new culture. You will have new career opportunities, a different lifestyle and meet all kind of different people with different backgrounds. You will experience the local cuisine, improve your language and will become more flexible and adaptable to new situations.
              </p>
              <p>Of course there is also a downside of being an expat some people get homesick because they miss their family and own culture. Studies conducted by various authorities show that of all the British that immigrate one fifth will return home early, while others say the number is even close to one third. For many British expats homesickness is the number one reason to move back.</p>
