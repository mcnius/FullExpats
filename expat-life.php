              <p>Often things will get easier if there is someone who helps you through the initial difficult first couple of months. Someone who knows the culture and is familiar with different customs and procedures in the new country. Someone who can help you with for example how the public transport works and where to find local food. How to get the right insurance and how to travel around in the Netherlands and Europe. </p>
              <p>Another important factor which can help you to feel at home is interacting with new people and make new friends. It is important to join sport-clubs and attending dinner parties at work so you will meet new people with different backgrounds and will be able to build up a social life. 
                </p>
              <p>A lot of expats go to a new country unprepared not knowing what to expect. Reading yourself into a new country before you move is important so you know what to expect and you will not be completely overwhelmed by the new culture. </p>
